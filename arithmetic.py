def add(num1, num2):
    return num1+num2

def subtract(num1, num2):
    return num1-num2

def multiply(num1, num2):
    return float(num1)*float(num2)

def divide(num1, num2):
    floatnum1=float(num1)
    floatnum2=float(num2)
    return floatnum1/floatnum2

def square(num1):
    return num1**2

def cube(num1):
    return num1**3

def power(num1, num2):
    return num1**num2

def mod(num1, num2):
    return num1%num2